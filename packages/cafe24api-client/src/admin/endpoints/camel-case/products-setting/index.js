import register from '../../products-setting/index';
import {
  convertToCamelCase,
  convertToSnakeCase,
  formatCamelCasedOptions,
} from '../../../../utils/index';

export default (cls) => {
  register(cls);

  const retrieveProductSettings = cls.prototype.retrieveProductSettings;
  cls.prototype.retrieveProductSettings = async function (input, options) {
    const response = await retrieveProductSettings.call(
      this,
      convertToSnakeCase(input),
      formatCamelCasedOptions(options),
    );
    return {
      ...response,
      data: convertToCamelCase(response.data),
    };
  };
};
