import register from '../../mobile-setting/index';
import {
  convertToCamelCase,
  convertToSnakeCase,
  optionsToSnakeCase,
} from '../../../../utils/index';

export default (cls) => {
  register(cls);

  const retrieveMobileSettings = cls.prototype.retrieveMobileSettings;
  cls.prototype.retrieveMobileSettings = async function (input, options) {
    const response = await retrieveMobileSettings.call(
      this,
      convertToSnakeCase(input),
      optionsToSnakeCase(options),
    );
    return {
      ...response,
      data: convertToCamelCase(response.data),
    };
  };
};
