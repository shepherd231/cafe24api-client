import register from '../../menus/index';
import {
  convertToCamelCase,
  convertToSnakeCase,
  optionsToSnakeCase,
} from '../../../../utils/index';

export default (cls) => {
  register(cls);

  const retrieveMenus = cls.prototype.retrieveMenus;
  cls.prototype.retrieveMenus = async function (input, options) {
    const response = await retrieveMenus.call(
      this,
      convertToSnakeCase(input),
      optionsToSnakeCase(options),
    );
    return {
      ...response,
      data: convertToCamelCase(response.data),
    };
  };
};
