import register from '../../sms-setting/index';
import {
  convertToCamelCase,
  convertToSnakeCase,
  optionsToSnakeCase,
} from '../../../../utils/index';

export default (cls) => {
  register(cls);

  const retrieveSmsSettings = cls.prototype.retrieveSmsSettings;
  cls.prototype.retrieveSmsSettings = async function (input, options) {
    const response = await retrieveSmsSettings.call(
      this,
      convertToSnakeCase(input),
      optionsToSnakeCase(options),
    );
    return {
      ...response,
      data: convertToCamelCase(response.data),
    };
  };
};
